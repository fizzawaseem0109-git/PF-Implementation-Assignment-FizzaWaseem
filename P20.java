 public class P20 {    
    public static void main(String[] args) {

        System.out.println("---Printing with Escape Sequences ---");

        // (\t for tab, \n for newline, \\ for backslash)
        System.out.println("C:\\Windows>");
        
        //  (\' for single quote, \t for tab)
        System.out.println("\'P\'\t\'A\'\t\'K\'");
        
        // (\" for double quote)
        System.out.println("\"Pakistan\"");
    }
 }