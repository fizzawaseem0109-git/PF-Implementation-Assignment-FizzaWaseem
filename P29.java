public class P29 {
    public static void main(String[] args) {

        System.out.println("---'if' Statement (n > 100) ---");
        
        // if value is greater than 100
        int n = 150; 

        System.out.println("Current value of n: " + n);

        if (n > 100) {
            System.out.println("The value of 'n' is greater than 100. The condition was met.");
        }
        
        // if value is less than 100
        int m = 50;

        System.out.println("Current value of n: " + m);

        if (m > 100) {
             System.out.println(" the value of 'n' is greater than 100. The condition was met.");
        }
        
    }     

}
